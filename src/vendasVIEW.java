    public void montarTabela() {
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaProdutosVendidos.getModel();
        modeloTabela.setRowCount(0);
        
        ProdutosDAO produtoDAO = new ProdutosDAO();
        
        ArrayList<ProdutosDTO> lista = produtoDAO.listarProdutosVendidos();
        
        for (ProdutosDTO produto : lista) {
            String[] linha = {
              String.valueOf(produto.getId()),
              produto.getNome(),
              String.valueOf(produto.getValor()),
              produto.getStatus()
            };
            
            modeloTabela.addRow(linha);
        }
        tabelaProdutosVendidos.setModel(modeloTabela);
    }
    
